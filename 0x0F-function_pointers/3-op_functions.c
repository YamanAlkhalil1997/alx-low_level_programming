#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first int
 * @b: the second int
 *
 * return the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * asd
 *
 * asd
 * as
*/

int op_sub(int a, int b)
{
	return (a - b);
}
/*
 * asdasd
 * asd
 * as
 * da
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * asd
 * asd
 * asd
*/

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
